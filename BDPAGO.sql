CREATE PROC SP_CRUD_PAGO(
@ID_PAYMENT BIGINT,
@CODE_PRODUCTO CHAR(4),
@CODE_CLIENT INT,
@CODE_USER INT,
@PAYMENT_DATE DATE,
@PAYMENT_VALUE DECIMAL(10,2),
@NOTE NVARCHAR(MAX),
@STATUS BIT,
@OPTION CHAR(1)
) AS BEGIN 
		IF(@OPTION = 'C') BEGIN
				
				INSERT INTO PAGO(CODE_PRODUCT,CODE_CLIENT,CODE_USER,PAYMENT_DATE,PAYMENT_VALUE,NOTE,STATUS) VALUES (@CODE_PRODUCTO,@CODE_CLIENT,@CODE_USER,@PAYMENT_DATE,@PAYMENT_VALUE,@NOTE,@STATUS)


		END
		IF(@OPTION = 'R') BEGIN 

			SELECT 
				ID_PAYMENT AS ID,
				CODE_PRODUCT AS PRODUCTO,
				CODE_CLIENT AS CLIENTE, 
				CODE_USER AS USUARIO,
				PAYMENT_DATE AS FECHA,
				PAYMENT_VALUE AS TOTAL, 
				NOTE AS NOTA, 
				STATUS AS ACTIVO 
			FROM PAGO 
			ORDER BY PAYMENT_DATE DESC
			

		END
		IF(@OPTION = 'U') BEGIN
				
				UPDATE PAGO SET 
					CODE_PRODUCT=@CODE_PRODUCTO,
					CODE_CLIENT=@CODE_CLIENT,
					CODE_USER=@CODE_USER,
					PAYMENT_DATE=@PAYMENT_DATE,
					PAYMENT_VALUE=@PAYMENT_VALUE,
					NOTE=@NOTE,
					STATUS=@STATUS
				WHERE ID_PAYMENT = @ID_PAYMENT

		END
		IF(@OPTION = 'D') BEGIN
			
			DELETE FROM PAGO WHERE ID_PAYMENT=@ID_PAYMENT

		END

END
-------------------------------------------------

exec SP_CRUD_PAGO 0,'',0,0,'',0,'',0,'R'

