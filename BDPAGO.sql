CREATE PROC SP_REPORT_PAGO(
	@CODE_PRODUCT CHAR(4),
	@DNI NCHAR(8),
	@CODE_USER INT,
	@OPTION TINYINT
)AS BEGIN
IF(@OPTION = 1) BEGIN 
	 
		
SELECT X.*,
	X.[ENERO 2019]+X.[FEBRERO 2019]+X.[MARZO 2019]
	
	FROM (
	SELECT PR.DESCRIPTION [PRODUCTO]
	, C.NAME + ' ' + LASTNAME [CLIENTE]
	, SUM( IIF( YEAR(P.PAYMENT_DATE) = 2019 AND MONTH(P.PAYMENT_DATE) = 1,P.PAYMENT_VALUE,0))[ENERO 2019]
	, SUM( IIF( YEAR(P.PAYMENT_DATE) = 2019 AND MONTH(P.PAYMENT_DATE) = 2,P.PAYMENT_VALUE,0)) [FEBRERO 2019]
	, SUM( IIF( YEAR(P.PAYMENT_DATE) = 2019 AND MONTH(P.PAYMENT_DATE) = 3,P.PAYMENT_VALUE,0)) [MARZO 2019]
	FROM CLIENTE C
	INNER JOIN PAGO P ON C.CODE_CLIENT = P.CODE_CLIENT
	INNER JOIN PRODUCTO PR ON P.CODE_PRODUCT = PR.CODE_PRODUCT AND PR.CODE_PRODUCT LIKE @CODE_PRODUCT
	GROUP BY PR.DESCRIPTION, C.NAME + ' ' + LASTNAME
	
	) X



END
IF(@OPTION = 2) BEGIN 
	SELECT TODO.[CLIENTE],TODO.[PRODUCTO],TODO.[ENERO],TODO.[FEBRERO],TODO.[MARZO],TODO.[TOTAL],(TODO.[TOTAL])/3 AS PROMEDIO--,TODO.[TOTAL]/SUM(TODO.[TOTAL])

	FROM(
	SELECT CLI.NAME +' '+LASTNAME [CLIENTE],
	PRO.DESCRIPTION [PRODUCTO],
	SUM(IIF(YEAR(PAG.PAYMENT_DATE)=2019 AND DATENAME(MONTH,PAG.PAYMENT_DATE)='ENERO',PAG.PAYMENT_VALUE,0)) [ENERO],
	SUM(IIF(YEAR(PAG.PAYMENT_DATE)=2019 AND DATENAME(MONTH,PAG.PAYMENT_DATE)='FEBRERO',PAG.PAYMENT_VALUE,0)) [FEBRERO],
	SUM(IIF(YEAR(PAG.PAYMENT_DATE)=2019 AND DATENAME(MONTH,PAG.PAYMENT_DATE)='MARZO',PAG.PAYMENT_VALUE,0)) [MARZO],
	(SUM(IIF(YEAR(PAG.PAYMENT_DATE)=2019 AND (
			DATENAME(MONTH,PAG.PAYMENT_DATE)='ENERO' OR
			DATENAME(MONTH,PAG.PAYMENT_DATE)='FEBRERO' OR
			DATENAME(MONTH,PAG.PAYMENT_DATE)='MARZO')
														,PAG.PAYMENT_VALUE,0))) [TOTAL]
	
	
	

	FROM PRODUCTO PRO
	INNER JOIN PAGO PAG
	ON PAG.CODE_PRODUCT = PRO.CODE_PRODUCT
	INNER JOIN CLIENTE CLI
	ON CLI.CODE_CLIENT = PAG.CODE_CLIENT  AND CLI.DNI = @DNI
	GROUP BY CLI.NAME + ' ' + LASTNAME,PRO.DESCRIPTION
	) TODO
END
IF(@OPTION = 3) BEGIN 
SELECT TODO.[USUARIO],TODO.[PRODUCTO],TODO.[ENERO],TODO.[FEBRERO],TODO.[MARZO],TODO.[TOTAL],(TODO.[TOTAL])/3 AS PROMEDIO--,TODO.[TOTAL]/SUM(TODO.[TOTAL])

	FROM(
	SELECT USU.USERNAME [USUARIO],
	PRO.DESCRIPTION [PRODUCTO],
	SUM(IIF(YEAR(PAG.PAYMENT_DATE)=2019 AND DATENAME(MONTH,PAG.PAYMENT_DATE)='ENERO',PAG.PAYMENT_VALUE,0)) [ENERO],
	SUM(IIF(YEAR(PAG.PAYMENT_DATE)=2019 AND DATENAME(MONTH,PAG.PAYMENT_DATE)='FEBRERO',PAG.PAYMENT_VALUE,0)) [FEBRERO],
	SUM(IIF(YEAR(PAG.PAYMENT_DATE)=2019 AND DATENAME(MONTH,PAG.PAYMENT_DATE)='MARZO',PAG.PAYMENT_VALUE,0)) [MARZO],
	(SUM(IIF(YEAR(PAG.PAYMENT_DATE)=2019 AND (
			DATENAME(MONTH,PAG.PAYMENT_DATE)='ENERO' OR
			DATENAME(MONTH,PAG.PAYMENT_DATE)='FEBRERO' OR
			DATENAME(MONTH,PAG.PAYMENT_DATE)='MARZO')
														,PAG.PAYMENT_VALUE,0))) [TOTAL]
	
	
	

	FROM PRODUCTO PRO
	INNER JOIN PAGO PAG
	ON PAG.CODE_PRODUCT = PRO.CODE_PRODUCT
	INNER JOIN USUARIO_CAJA USU
	ON USU.CODE_USER = PAG.CODE_USER  AND USU.CODE_USER = @CODE_USER
	GROUP BY USU.USERNAME,PRO.DESCRIPTION
	) TODO
